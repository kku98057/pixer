const nftTabs = document.querySelectorAll(".tab_slide");
let nftArray = [...nfts];
nftTabs.forEach((nftTab, idx) => {
  nftTab.addEventListener("click", () => {
    nftTabs.forEach((item, index) => {
      const target = item.dataset.nftTab;
      if (idx === index) {
        item.classList.add("active");
        if (target === "all") {
          nftArray = [...nfts];
        } else {
          nftArray = [...nfts].filter((item) => item.title === target);
        }
      } else {
        item.classList.remove("active");
      }
    });
    initialSlider();
  });
});

const slideWrapper = document.querySelector(".nft_contents_slide-wrapper");
let slider;
const slideLists = async () => {
  if (nftArray.length <= 0) {
    slideWrapper.innerHTML = `<p class="noitem">NO ITEMS</p>`;
    return;
  } else {
    slideWrapper.innerHTML = nftArray
      .map(
        (item) => ` <div class="swiper-slide nft_contents_slide">
  <div class="nft_contents_slide-img">
      <img src=${item.src} alt=${item.name}>
  </div>
  <div class="nft_contents_slide-txt">
      <span>${item.title}</span>
  </div>
</div>`
      )
      .join("");
    await sliders();
  }
};

const sliders = () => {
  if (slider) {
    // 이전 슬라이더 객체가 있으면 파괴
    slider.destroy();
  }
  slider = new Swiper(".nft_contents_slider", {
    slidesPerView: "auto",
    slideActiveClass: "active",
    spaceBetween: 20,
    initialSlide: 4,
    autoplay: {
      delay: 3000,
      disableOnInteraction: false,
    },
    centeredSlides: true,
    navigation: {
      prevEl: `.nft_contents_prev`,
      nextEl: `.nft_contents_next`,
    },
  });
};

const initialSlider = () => {
  slideLists();
};
initialSlider();
